// ???????????????????????????????????????????????????????????????????????????????
// BLAZOR DEVELOPER TOOLS - bridge.js (Page Context)
// ???????????????????????????????????????????????????????????????????????????????
//
// Runs in the page context (not content script context).
// Has access to window.blazorDevTools which is registered by the .NET library.
//
// Handles communication between content script and .NET runtime.
//
// ???????????????????????????????????????????????????????????????????????????????

(function () {
    'use strict';

    console.log('[BDT Bridge] Initializing in page context');

    // ???????????????????????????????????????????????????????????????????????????
    // Wait for blazorDevTools to be registered by .NET
    // ???????????????????????????????????????????????????????????????????????????

    function waitForBlazorDevTools(): void {
        const checkInterval = setInterval(() => {
            const devTools = (window as any).blazorDevTools;
            if (devTools && devTools._dotNetRef) {
                clearInterval(checkInterval);
                onBlazorReady(devTools);
            }
        }, 100);

        // Give up after 30 seconds
        setTimeout(() => {
            clearInterval(checkInterval);
        }, 30000);
    }

    function onBlazorReady(devTools: any): void {
        console.log('[BDT Bridge] Connected to Blazor runtime');

        // Notify content script
        window.postMessage({
            source: 'blazor-devtools-bridge',
            type: 'READY',
            data: { circuitId: devTools._circuitId || null },
        }, '*');

        // Listen for invoke requests from content script
        window.addEventListener('message', async (event) => {
            if (event.source !== window) return;
            if (event.data?.source !== 'blazor-devtools-content') return;
            if (event.data?.type !== 'INVOKE') return;

            const { method, args, requestId } = event.data.data;

            try {
                const result = await devTools._dotNetRef.invokeMethodAsync(method, ...args);
                window.postMessage({
                    source: 'blazor-devtools-bridge',
                    type: 'RESPONSE',
                    data: { requestId, result },
                }, '*');
            } catch (error: any) {
                window.postMessage({
                    source: 'blazor-devtools-bridge',
                    type: 'RESPONSE',
                    data: { requestId, result: { error: error.message || 'Unknown error' } },
                }, '*');
            }
        });
    }

    waitForBlazorDevTools();
})();
