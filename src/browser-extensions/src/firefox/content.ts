// ???????????????????????????????????????????????????????????????????????????????
// BLAZOR DEVELOPER TOOLS - content.ts (Firefox - CSP Compatible)
// ???????????????????????????????????????????????????????????????????????????????
//
// Content script injected into web pages.
// Bridges between the extension (background/panel) and the page context.
//
// This version loads bridge.js as an external file to comply with CSP.
//
// Message Flow:
//   Panel ? Background ? Content (this) ? Bridge.js ? window.blazorDevTools._dotNetRef
//   Panel ? Background ? Content (this) ? Bridge.js ? [response]
//
// ???????????????????????????????????????????????????????????????????????????????

(function() {
    let bridgeLoaded = false;
    let blazorReady = false;
    const pendingRequests = new Map<string, (response: unknown) => void>();

// ???????????????????????????????????????????????????????????????????????????????
// INJECT BRIDGE SCRIPT INTO PAGE CONTEXT
// ???????????????????????????????????????????????????????????????????????????????

function injectBridge(): void {
    if (bridgeLoaded) return;
    bridgeLoaded = true;

    const script = document.createElement('script');
    script.src = browser.runtime.getURL('bridge.js');
    script.onload = () => {
        console.log('[BDT Content] Bridge script loaded');
    };
    script.onerror = (e) => {
        console.error('[BDT Content] Failed to load bridge script:', e);
    };
    (document.head || document.documentElement).appendChild(script);
}

// ???????????????????????????????????????????????????????????????????????????????
// LISTEN FOR MESSAGES FROM BRIDGE (page context)
// ???????????????????????????????????????????????????????????????????????????????

window.addEventListener('message', (event) => {
    if (event.source !== window) return;
    if (event.data?.source !== 'blazor-devtools-bridge') return;

    const { type, data } = event.data;

    switch (type) {
        case 'READY':
            blazorReady = true;
            console.log('[BDT Content] Blazor bridge ready, circuit:', data.circuitId);
            browser.runtime.sendMessage({ type: 'BLAZOR_DETECTED', circuitId: data.circuitId });
            break;

        case 'DISCONNECTED':
            blazorReady = false;
            console.log('[BDT Content] Blazor disconnected');
            browser.runtime.sendMessage({ type: 'BLAZOR_DISCONNECTED' });
            break;

        case 'RESPONSE':
            // Relay response back to extension
            const { requestId, result, error } = data;
            const resolve = pendingRequests.get(requestId);
            if (resolve) {
                pendingRequests.delete(requestId);
                const errorMessage = error ?? result?.error;
                if (errorMessage) {
                    resolve({ error: errorMessage });
                } else {
                    resolve({ data: result });
                }
            }
            break;
    }
});

// ???????????????????????????????????????????????????????????????????????????????
// LISTEN FOR REQUESTS FROM PANEL (via background)
// ???????????????????????????????????????????????????????????????????????????????

browser.runtime.onMessage.addListener((message) => {
    if (message.type !== 'PANEL_REQUEST') return false;

    const { method, args } = message;

    if (!blazorReady) {
        return Promise.resolve({ error: 'Blazor DevTools not ready' });
    }

    // Generate unique request ID
    const requestId = Math.random().toString(36).slice(2) + Date.now().toString(36);

    // Forward to bridge in page context
    window.postMessage({
        source: 'blazor-devtools-content',
        type: 'INVOKE',
        data: { method, args, requestId },
    }, '*');

    // Wait for response
    return new Promise((resolve) => {
        pendingRequests.set(requestId, resolve);
        // Timeout after 10s
        setTimeout(() => {
            if (pendingRequests.has(requestId)) {
                pendingRequests.delete(requestId);
                resolve({ error: 'Request timeout' });
            }
        }, 10000);
    });
});

// ???????????????????????????????????????????????????????????????????????????????
// INITIALIZE
// ???????????????????????????????????????????????????????????????????????????????

injectBridge();
console.log('[BDT Content] Initialized');

})();

export {};
